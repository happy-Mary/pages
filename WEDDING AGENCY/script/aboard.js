// Chehia
        document.getElementById('moreCheh').style.display = 'inline';
        document.getElementById('moreContentCheh').style.display = 'none';
        
        function showSectionCheh() {
        document.getElementById('moreContentCheh').style.display = 'block';
        document.getElementById('moreCheh').style.display = 'none';
        document.getElementById('lessCheh').style.display = 'inline';
}

        function hideSectionCheh() {
        document.getElementById('moreContentCheh').style.display = 'none';
        document.getElementById('moreCheh').style.display = 'inline';
        document.getElementById('lessCheh').style.display = 'none';
}
